package com.sport.pushupsapp;

public class State {
    String img, name, xp;
    public State(String img, String name, String xp) {
        this.img = img;
        this.name = name;
        this.xp = xp;
    }

    public String getImg() {
        return img;
    }

    public String getName() {
        return name;
    }

    public String getXp() {
        return xp;
    }
}
